// detInput builds a deterministic pseudo-random set of input blocks for a net, with k
// teammate rows and m opponent rows.
func detInput(n *Net, seed int64, k, m int) (self, ball, global, team, opp []float32) {
	r := rand.New(rand.NewSource(seed))
	fill := func(sz int) []float32 {
		s := make([]float32, sz)
		for i := range s {
			s[i] = r.Float32()*2 - 1
		}
		return s
	}
	return fill(n.SelfDim), fill(n.BallDim), fill(n.GlobalDim), fill(k * n.EntDim), fill(m * n.EntDim)
}

func TestLoadDefaultAndForward(t *testing.T) {
	n, err := LoadDefault()
	if err != nil {
		t.Fatalf("LoadDefault: %v", err)
	}
	if n.TotalHeadWidth != 34 {
		t.Errorf("TotalHeadWidth = %d, want 34", n.TotalHeadWidth)
	}
	ws := n.NewWorkspace()
	self, ball, global, team, opp := detInput(n, 7, 3, 4)
	logits := n.Forward(ws, self, ball, global, team, opp)
	if len(logits) != n.TotalHeadWidth {
		t.Fatalf("logits len = %d, want %d", len(logits), n.TotalHeadWidth)
	}
	for i, v := range logits {
		if math.IsNaN(float64(v)) || math.IsInf(float64(v), 0) {
			t.Fatalf("logit %d not finite: %v", i, v)
		}
	}
	// Empty entity groups must be handled (1v0-style rosters).
	logits0 := n.Forward(ws, self, ball, global, nil, nil)
	if len(logits0) != n.TotalHeadWidth {
		t.Fatalf("empty-roster logits len = %d", len(logits0))
	}
}

