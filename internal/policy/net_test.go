// TestForwardGoldenVector is the Go<->Python parity gate. It is skipped until the exporter
// (training/export.py) has written testdata/golden_forward.bin. Once present, the Go forward
// pass must match the Python reference bit-for-bit (proving Go inference == the trained net).
//
// Self-describing record format (little-endian), so policy stays game-agnostic:
//
//	uint32 count
//	per record: uint32 nTeam, uint32 nOpp,
//	            float32 self[SelfDim], ball[BallDim], global[GlobalDim],
//	            float32 team[nTeam*EntDim], opp[nOpp*EntDim],
//	            float32 logits[TotalHeadWidth]
func TestForwardGoldenVector(t *testing.T) {
	const path = "testdata/golden_forward.bin"
	raw, err := os.ReadFile(path)
	if err != nil {
		t.Skipf("no parity vectors yet (generated by training/export.py): %v", err)
	}
	n, err := LoadDefault()
	if err != nil {
		t.Fatalf("LoadDefault: %v", err)
	}
	ws := n.NewWorkspace()
	off := 0
	u32 := func() int {
		v := int(binary.LittleEndian.Uint32(raw[off:]))
		off += 4
		return v
	}
	f32n := func(k int) []float32 {
		s := make([]float32, k)
		for i := range s {
			s[i] = math.Float32frombits(binary.LittleEndian.Uint32(raw[off:]))
			off += 4
		}
		return s
	}
	count := u32()
	for c := 0; c < count; c++ {
		nTeam := u32()
		nOpp := u32()
		self := f32n(n.SelfDim)
		ball := f32n(n.BallDim)
		global := f32n(n.GlobalDim)
		team := f32n(nTeam * n.EntDim)
		opp := f32n(nOpp * n.EntDim)
		want := f32n(n.TotalHeadWidth)
		got := n.Forward(ws, self, ball, global, team, opp)
		for i := range want {
			if math.Float32bits(got[i]) != math.Float32bits(want[i]) {
				t.Fatalf("record %d logit %d: got %v (%08x) want %v (%08x)",
					c, i, got[i], math.Float32bits(got[i]), want[i], math.Float32bits(want[i]))
			}
		}
	}
}
